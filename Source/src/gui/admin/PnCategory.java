    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        int i = tbcat.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to modify!");
            return;
        }
        int catID = Integer.parseInt(tbcat.getValueAt(i, 0).toString().trim());
        String name = tbcat.getValueAt(i, 1).toString().trim();
        String description = tbcat.getValueAt(i, 2).toString().trim();
        Category cat = new Category(name, description, catID);
        ad.trypane.removeAll();
        ad.trypane.add(new PnCategoryModify1(ad, cat));
        ad.trypane.validate();
        ad.trypane.updateUI();
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int i = tbcat.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to delete!");
            return;
        } else {
            int test = JOptionPane.showConfirmDialog(null, "are you sure!");

            if (test == JOptionPane.YES_OPTION) {
                CategoryList catl = new CategoryList();
                if (catl.delete(Integer.parseInt(tbcat.getValueAt(i, 0).toString()))) {
                    JOptionPane.showMessageDialog(null, "delete successful!");
                    model.setRowCount(0);
                    initModel();
                } else {
                    JOptionPane.showMessageDialog(null, "Error delete!");
                }
            }

        }

    }//GEN-LAST:event_btnDeleteActionPerformed


