    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String name = txtName.getText().trim();
        String description = txtDesc.getText().trim();
        int catID =0;
        
        if (name.length()<=0) {
            JOptionPane.showMessageDialog(null, "name can not bank!");
            return;
        }
        if (description.length()<=0) {
            JOptionPane.showMessageDialog(null, "description can not bank!");
            return;
        }
        Category cat = new Category(name, description, catID);
        CategoryList catl = new CategoryList();
        
        if (catl.insert(cat)) {
             JOptionPane.showMessageDialog(null, "Insert successful!");
        }else{
         JOptionPane.showMessageDialog(null, "Insert Error!");
        }
    }//GEN-LAST:event_btnOKActionPerformed

