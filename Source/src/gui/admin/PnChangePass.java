    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed
       String old = txtOld.getText().trim();
       String newPass = txtNew.getText().trim();
       String reNew = txtRe.getText().trim();
        if (old.length()<=0 || newPass.length()<=0 || reNew.length()<=0 ) {
             JOptionPane.showMessageDialog(null, "you must fill all feild");
            return;
        }
        if (!old.equals(ad.pass)) {
            JOptionPane.showMessageDialog(null, "your old password is not correct! please, try again.");
            return;
        }
        if (!reNew.equals(newPass)) {
            JOptionPane.showMessageDialog(null, "Confirm password is not correct!");
            return;
        }
        UserList user = new UserList();
        for (User o : user.getList()) {
            if (ad.userName.equals(o.userID)) {
                o.Password = reNew;
                if(user.update(o)==true){
                    JOptionPane.showMessageDialog(null, "change password successful!");
                }
            }
        }
    }//GEN-LAST:event_btnokActionPerformed

