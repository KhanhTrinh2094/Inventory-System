    private void initModel() {
        model = (DefaultTableModel) tbUser.getModel();
        UserList catl = new UserList();
        for (User o : catl.getList()) {
            Vector v = new Vector();
            v.add(o.userID);
            v.add(o.fullName);
            v.add(o.RoleID);
            if (o.Sex.equals("0")) {
                v.add("Female");
            } else {
                v.add("Male");
            }
            v.add(o.DOB);
            v.add(o.Phone);
            v.add(o.Adress);
            model.addRow(v);

        }

    }
