    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int i = tbUser.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to delete!");
            return;
        } else {
            int test = JOptionPane.showConfirmDialog(null, "are you sure!");

            if (test == JOptionPane.YES_OPTION) {
                UserList catl = new UserList();
                if (catl.delete(tbUser.getValueAt(i, 0).toString().trim())) {
                    JOptionPane.showMessageDialog(null, "delete successful!");
                    model.setRowCount(0);
                    initModel();
                } else {
                    JOptionPane.showMessageDialog(null, "Error delete!");
                }
            }

        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int i = tbUser.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to modify!");
            return;
        }
        String userID = tbUser.getValueAt(i, 0).toString().trim();
        String fullName = tbUser.getValueAt(i, 1).toString().trim();
        String Password = "";
        UserList ul = new UserList();
        for (User o : ul.getList()) {
            if (userID.equals(o.userID.trim())) {
                Password = o.Password;
            }
        }

        String Sex = "";
        String DOB = tbUser.getValueAt(i, 4).toString().trim();
        String Phone = tbUser.getValueAt(i, 5).toString().trim();
        String Adress = tbUser.getValueAt(i, 6).toString().trim();
        int role = 0;

        User ur = new User(userID, fullName, Password, Sex, DOB, Phone, Adress, role);

        ad.trypane.removeAll();
        ad.trypane.add(new PnUserModify(ad, ur));
        ad.trypane.validate();
        ad.trypane.updateUI();
    }//GEN-LAST:event_jButton5ActionPerformed


