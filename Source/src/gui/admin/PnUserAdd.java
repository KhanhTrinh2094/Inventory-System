    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String userID = txtusername.getText().trim();
        String fullName = txtFullname.getText().trim();
        String Password = txtPass.getText().trim();
        String Sex = txtsex.getSelectedItem().toString().trim().equals("male") ? "true" : "false";
        String DOB = txtDob.getText().trim();
        String Phone = txtPhone.getText().trim();
        String Adress = txtAddress.getText().trim();
        String role = txtRole.getSelectedItem().toString().trim();
        int roleID = 0;
        UserList userList = new UserList();
        for (User o : userList.getList()) {
            if (userID.equals(o.userID.trim())) {
                 JOptionPane.showMessageDialog(null, "userID have already existed! Please enter another.");
            return;
            }
        }
        

        if (userID.length() <= 4) {
            JOptionPane.showMessageDialog(null, "userID must have at least 5 chracter!");
            return;
        }
        if (fullName.length() <= 0) {
            JOptionPane.showMessageDialog(null, "fullName can not bank!");
            return;
        }
        if (Password.length() <= 5) {
            JOptionPane.showMessageDialog(null, "Password must have at least 6 chracter!");
            return;
        }
        if (DOB.length() <= 0) {
            JOptionPane.showMessageDialog(null, "date of birth can not bank!");
            return;
        }
        if (DOB.trim().length() > 0) {
            Pattern p = Pattern.compile("^[0-9]{4}\\-[0-9]{2}\\-[0-9]{2}$");
            Matcher m = p.matcher(DOB);

            if (m.matches() == false) {
                JOptionPane.showMessageDialog(null, "date must have format: YYYY-MM-DD");
                return;
            }
        }
        if (Adress.length() <= 0) {
            JOptionPane.showMessageDialog(null, "Address of birth can not bank!");
            return;
        }
        Pattern k = Pattern.compile("^[0-9]+$");
        Matcher m1 = k.matcher(Phone);

        if (m1.matches() == false) {
            JOptionPane.showMessageDialog(null, "Phone must be a number!");
            return;
        }
        RoleList rl = new RoleList();
        for (Role o : rl.getList()) {
            if (role.equals(o.roleName.trim())) {
                roleID = o.RoleID;
            }
        }
        
        User u = new User(userID, fullName, Password, Sex, DOB, Phone, Adress, roleID);
        UserList catl = new UserList();

        if (catl.insert(u)) {
            JOptionPane.showMessageDialog(null, "Insert successful!");
        } else {
            JOptionPane.showMessageDialog(null, "Insert Error!");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

