    private void initModel() {
        model = new DefaultTableModel();
        model = (DefaultTableModel) tbWare.getModel();
        WarehouseList wa = new WarehouseList();
        for (Warehouse o : wa.getList()) {
            model.addRow(o.toVector());
        }

    }
