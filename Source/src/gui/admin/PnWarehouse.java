    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        int i = tbWare.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to modify!");
            return;
        }
        String id = tbWare.getValueAt(i, 0).toString().trim();
        String name = tbWare.getValueAt(i, 1).toString().trim();
        String adress = tbWare.getValueAt(i, 2).toString().trim();
        String phone = tbWare.getValueAt(i, 3).toString().trim();

        ad.trypane.removeAll();
        ad.trypane.add(new PnWarehousemodify(ad, id, name, adress, phone));
        ad.trypane.validate();
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int i = tbWare.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(null, "please, choose one row to delete!");
            return;
        } else {
            int test = JOptionPane.showConfirmDialog(null, "are you sure!");

            if (test == JOptionPane.YES_OPTION) {
                WarehouseList catl = new WarehouseList();
                if (catl.delete(tbWare.getValueAt(i, 0).toString().trim())) {
                    JOptionPane.showMessageDialog(null, "delete successful!");
                    model.setRowCount(0);
                    initModel();
                } else {
                    JOptionPane.showMessageDialog(null, "Error delete!");
                }
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed


