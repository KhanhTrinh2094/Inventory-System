    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       String id = txtId.getText().trim();
       String name = txtName.getText().trim();
       String adress = txtAdre.getText().trim();
       String phone = txtPhone.getText().trim();
       
        WarehouseList userList = new WarehouseList();
        for (Warehouse o : userList.getList()) {
            if (id.equals(o.warehouseID.trim())) {
                 JOptionPane.showMessageDialog(null, "warehouseID have already existed! Please enter another.");
            return;
            }
        }
        Pattern p = Pattern.compile("^[0-9]+$");
        Matcher m = p.matcher(phone);

        if (m.matches() == false) {
            JOptionPane.showMessageDialog(null, "phone must be a number!");
            return;
        }
        
        if (id.length()<5) {
            JOptionPane.showMessageDialog(null, "Id must have at least 5 chracter!");
            return;
        }
        if (name.length()<=0) {
            JOptionPane.showMessageDialog(null, "name can not bank!");
            return;
        }
        if (adress.length()<=0) {
            JOptionPane.showMessageDialog(null, "address can not bank!!");
            return;
        }
        if (phone.length()<=0) {
            JOptionPane.showMessageDialog(null, "phone can not bank!");
            return;
        }
        Warehouse w = new Warehouse(id, name, adress, phone);
        WarehouseList wl = new WarehouseList();
        if (wl.insert(w)==true) {
             JOptionPane.showMessageDialog(null, "Insert successful!");
        }else{
             JOptionPane.showMessageDialog(null, "Error insert!");
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

