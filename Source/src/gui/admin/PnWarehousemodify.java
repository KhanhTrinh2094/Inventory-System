    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        String idm= txtId.getText().trim();
       String namem = txtName.getText().trim();
       String adressm = txtAdre.getText().trim();
       String phonem = txtPhone.getText().trim();
        
        if (idm.length()<5) {
            JOptionPane.showMessageDialog(null, "Id must have at least 5 chracter!");
            return;
        }
        if (namem.length()<=0) {
            JOptionPane.showMessageDialog(null, "name can not bank!");
            return;
        }
        if (adressm.length()<=0) {
            JOptionPane.showMessageDialog(null, "address can not bank!!");
            return;
        }
        if (phonem.length()<=0) {
            JOptionPane.showMessageDialog(null, "phone can not bank!");
            return;
        }
        Warehouse w = new Warehouse(idm, namem, adressm, phonem);
        WarehouseList wl = new WarehouseList();
        if (wl.update(w)==true) {
             JOptionPane.showMessageDialog(null, "Update successful!");
        }else{
             JOptionPane.showMessageDialog(null, "Error Update!");
        }
        
              
    }//GEN-LAST:event_btnOkActionPerformed

