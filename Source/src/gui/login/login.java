    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        UserList user = new UserList();
        int test = 0;
        int roleID = -1, level = -1;
        String userName = txtUserName.getText().trim();
        String Pass = txtPass.getText().trim();
        String fullname="";

        for (User u : user.getList()) {
            if (u.userID.equals(userName) && u.Password.equals(Pass)) {
                test++;
                roleID = u.RoleID;
                fullname = u.fullName;
            }

        }

        if (test == 0) {
            JOptionPane.showMessageDialog(null, "username and password are not correct! please try again!");
        }
        if (test != 0) {
            try {
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery("select * from tbRole");

                //xu ly ket qua
                //duyet cac dong trong rs => blist
                while (rs.next()) {
                    //doc du lieu cua cac cot tuong ung trong dong hien tai

                    if (roleID == rs.getInt(1)) {
                        level = rs.getInt(4);
                    }

                }
                //dong connection
                rs.close();
                //cn.close();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        if (level == 3) {
             man = new manHome(userName,Pass,fullname);
            man.setVisible(true);
            this.setVisible(false);
        } else if (level == 2) {
               manage = new FrmanagementHome(userName,Pass,fullname);
            manage.setVisible(true);
            this.setVisible(false);
        } else  if (level == 1) {
             admin = new FrAdmin(userName,Pass,fullname);
            admin.setVisible(true);
            this.setVisible(false);
        }else if (level != -1) {
            JOptionPane.showMessageDialog(null, "your database hava problem!");
        }


    }//GEN-LAST:event_btnLogInActionPerformed

