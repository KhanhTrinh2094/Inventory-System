    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        String qt = txtQuantity.getText().trim();
        Pattern p = Pattern.compile("^[0-9]+$");
        Matcher m = p.matcher(qt);

        if (m.matches() == false) {
            JOptionPane.showMessageDialog(null, "quantity must be a number!");
            return;
        }
        if (txtDetailID.getText().trim().length() < 5) {
            JOptionPane.showMessageDialog(null, "DetailID  must have at least 5 character!");
            return;
        }
         if (txtComment.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(null, "Comment field  can not bank!");
            return;
        }
        

        String detailID = txtDetailID.getText().trim();
        String name = cbName.getSelectedItem().toString().trim();
        int quantity = Integer.parseInt(qt);
        int price = Integer.parseInt(txtPrice.getText().trim());
        String comment = txtComment.getText().trim();
        
        IdetailList userList = new IdetailList();
        for (Idetail o : userList.getList()) {
            if (detailID.equals(o.detailID.trim())) {
                 JOptionPane.showMessageDialog(null, "detailID have already existed! Please enter another.");
            return;
            }
        }
        
        Vector v = new Vector();
        v.add(detailID);
        v.add(name);
        v.add(quantity);
        v.add(price);
        v.add(comment);
         int row = n.tbPrDe.getRowCount();
        for (int i = 0; i < row; i++) {
            if (n.tbPrDe.getValueAt(i,1).toString().equals(name)) {
                JOptionPane.showMessageDialog(null, "this product have already exit in the bill!");
                return;
            }
        }
        n.model.addRow(v);
        man.trypane.removeAll();
        man.trypane.add(n);

        man.trypane.validate();
        man.trypane.updateUI();


    }//GEN-LAST:event_btnOkActionPerformed

