    private void cbNameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbNameItemStateChanged
        ProductList pr = new ProductList();
        for (Product o : pr.getList()) {
            if (cbName.getSelectedItem().toString().trim().equals(o.name.toString().trim())) {
                txtPrice.setText(String.valueOf(o.unitPrice));
            }
        }
    }//GEN-LAST:event_cbNameItemStateChanged


