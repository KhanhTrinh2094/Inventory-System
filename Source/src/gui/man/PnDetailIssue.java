    private void initModel() {
        model = (DefaultTableModel) tbDetail.getModel();
        EdetailList del = new EdetailList();
        CategoryList catl = new CategoryList();

        // receiptNo = "no111";
        for (Edetail d : del.getList()) {
            if (receiptNo.equals(d.billNo.toString())) {
                Vector v = new Vector();
                Connection cn = Mylib.getCon();
                if (cn != null) {

                    try {
                        Statement st = cn.createStatement();
                        ResultSet rs = st.executeQuery("select * from tbProduct");

                        //xu ly ket qua
                        //duyet cac dong trong rs => blist
                        while (rs.next()) {
                            String ID = rs.getString(1);

                            if (d.productID.equals(ID)) {
                                String name = rs.getString(2);
                                int cat = rs.getInt(3);
                                String catName = "";
                                for (Category c : catl.getList()) {
                                    if (cat == c.catID) {
                                        catName = c.catName;
                                    }
                                }
                                int quan = rs.getInt(4);
                                int price = rs.getInt(5);
                                String unit = rs.getString(6);
                                String manu = rs.getString(8);

                                v.add(d.productID);
                                v.add(name);
                                v.add(catName);
                                v.add(quan);
                                v.add(price);
                                v.add(unit);
                                v.add(manu);
                                v.add(d.detailID);
                                v.add(d.billNo);
                                v.add(d.comment);

                            }

                        }
                        //dong connection
                        rs.close();
                        cn.close();

                    } catch (SQLException ex) {
                        ex.printStackTrace();
                    }
                }
                model.addRow(v);
            }
        }
    }
