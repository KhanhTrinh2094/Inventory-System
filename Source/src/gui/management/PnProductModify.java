    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String productID = txtProductID.getText().trim();
        String name = txtName.getText().trim();
        String Unit = txtUnit.getText().trim();
        String status = txtStatus.getText().trim();
        String manufacturer = txtManu.getText().trim();
        int catID = 2;
        String c = cbCat.getSelectedItem().toString().trim();

        int Quantity = pr.Quantity;
        int unitPrice = Integer.parseInt(txtPrice.getValue().toString().trim());

        if (productID.length() <= 4) {
            JOptionPane.showMessageDialog(null, "productID must have at least 5 chracter!");
            return;
        }
        if (name.length() <= 0) {
            JOptionPane.showMessageDialog(null, "name can not bank!");
            return;
        }
        if (Unit.length() <= 0) {
            JOptionPane.showMessageDialog(null, "Unit can not bank!");
            return;
        }
        if (manufacturer.length() <= 0) {
            JOptionPane.showMessageDialog(null, "manufacturer can not bank!");
            return;
        }

        if (status.length() <= 0) {
            JOptionPane.showMessageDialog(null, "status can not bank!");
            return;
        }
        Pattern k = Pattern.compile("^[0-9]+$");
        Matcher m1 = k.matcher(txtPrice.getValue().toString().trim());

        if (m1.matches() == false) {
            JOptionPane.showMessageDialog(null, "Price must be a number!");
            return;
        }

        CategoryList catl = new CategoryList();
        for (Category o : catl.getList()) {
            if (c.equals(o.catName.trim())) {
                catID = o.catID;
            }
        }

        Product p = new Product(productID, name, Unit, status, manufacturer, catID, Quantity, unitPrice);
        ProductList pl = new ProductList();
        if (pl.update(p)) {
            JOptionPane.showMessageDialog(null, "Update successful!");
        } else {
            JOptionPane.showMessageDialog(null, "Update Error!");
        }


    }//GEN-LAST:event_jButton5ActionPerformed

